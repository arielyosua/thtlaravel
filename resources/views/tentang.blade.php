        <h2>TENTANG GPIB</h2>
        <p>Gereja Protestan di Indonesia bagian Barat (disingkat GPIB) adalah kumpulan persekutuan umat percaya Kristen Protestan di Indonesia. GPIB merupakan bagian dari Gereja Protestan di Indonesia (GPI) yang pada jaman Hindia Belanda bernama De Protestantse Kerk In Westelijk Indonesie. Hal ini sebagaimana telah disetujui dan diputuskan melalui Surat Keputusan Wakil Tinggi Kerajaan Belanda di Indonesia No. 2, tanggal 1 Desember 1948.</p></br>
        <p>Teologi GPIB didasari ajaran Reformasi dari Yohanes Calvin, seorang tokoh Reformasi Gereja Protestan berkebangsaan Perancis yang di kemudian hari pindah ke Jenewa dan memimpin gereja di sana.</p></br>
        <p>Dalam pengakuan dan pemahaman imannya, GPIB mengaku bahwa Yesus Kristus adalah Tuhan, Anak Allah, Juruselamat Dunia dan Kepala Gereja yang adalah Sumber Hidup, sebagaimana disaksikan dalam Alkitab.</p></br>
        <p>Dalam menata dan mengembangkan panggilan dan pengutusan, GPIB menganut sistem Presbiterial Sinodal yang dilaksanakan oleh para Presbiter yaitu Pendeta, Penatua dan Diaken bersama seluruh anggota Jemaat GPIB.</p></br>
        <p>GPIB hadir di tengah dan bersama masyarakat, bangsa dan negara Republik Indonesia di 26 Provinsi dan yang berada di Sumatera, Jawa, Bali, Nusa Tenggara Barat, Kalimantan, Sulawesi Selatan, Sulawesi Barat, Sulawesi Tenggara, serta pulau-pulau sekitarnya sesuai dengan wilayah yang ditetapkan pada waktu GPIB didirikan sebagai Gereja yang mandiri.</p></br>
        <p>Ketika pertama kali terbentuk, GPIB mempunyai TUJUH buah Klasis (kini disebut Mupel atau Musyawarah Pelayanan) dengan 53 jemaat yaitu:</p>
        <ol>
            <li>Klasis Jabar meliputi 9 jemaat: Jakarta, Tanjung Priok, Jatinegara, Depok, Bogor, Cimahi, Bandung, Cirebon dan Sukabumi</li>
            <li>Klasis Jateng meliputi 6 jemaat: Semarang, Magelang, Yogyakarta, Cilacap, Nusakambangan dan Surakarta</li>
            <li>Klasis Jatim meliputi 12 jemaat: Madiun, Kediri, Madura, Surabaya, Mojokerto, Malang, Jember, Bondowoso, Banyuwangi, Singaraja, Denpasar dan Mataram</li>
            <li>Klasis Sumatra meliputi 7 jemaat: Sabang, Kutaraja, Medan, Pematang Siantar, Padang, Telukbayur dan Palembang</li>
            <li>Klasis Bangka & Riau meliputi 4 jemaat: Tanjung Pinang, Pangkal Pinang, Muntok dan Tanjungpandan</li>
            <li>Klasis Kalimantan meliputi 8 jemaat: Singkawang, Pontianak, Banjarmasin, Samarinda, Balikpapan, Tarakan, Sanga-sanga dan Kotabaru</li>
            <li>Klasis Sulawesi meliputi 7 jemaat: Makassar, Pare-pare, Watansopeng, Raha, Palopo, Bone dan Malino</li>
        </ol></br>
        <p>GPIB adalah anggota dari GPI, Persekutuan Gereja-gereja di Indonesia (PGI), Dewan Gereja-gereja Asia (CCA), Aliansi Gereja-gereja Reformasi se-Dunia (WARC), dan Dewan Gereja-gereja se-Dunia (WCC).</p>
